package com.chirag.pdf.dto;

public class ImageDetail {
	public double xPPI;
	public double yPPI;
	public double areaInPercentage;
	
	public double getxPPI() {
		return xPPI;
	}
	public void setxPPI(double xPPI) {
		this.xPPI = xPPI;
	}
	public double getyPPI() {
		return yPPI;
	}
	public void setyPPI(double yPPI) {
		this.yPPI = yPPI;
	}
	public double getAreaInPercentage() {
		return areaInPercentage;
	}
	public void setAreaInPercentage(double areaInPercentage) {
		this.areaInPercentage = areaInPercentage;
	}
	
}
